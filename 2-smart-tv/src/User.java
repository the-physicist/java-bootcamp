public class User {
    public static void main(String[] args) {
        // Create an instance of SmartTV
        SmartTv myTV = new SmartTv();

        // Turn on the TV
        myTV.powerOn();

        // Adjust the volume
        myTV.setVolume(30);

        // Change the channel
        myTV.setChannel(5);

        // Decrease the channel
        myTV.channelDown();

        // Increase the channel
        myTV.channelUp();

        // Turn off the TV
        myTV.powerOff();

         // Adjust the volume
        myTV.setVolume(50);

        // Change the channel
        myTV.setChannel(15);

        // Decrease the channel
        myTV.channelDown();

        // Increase the channel
        myTV.channelUp();
    }
}
